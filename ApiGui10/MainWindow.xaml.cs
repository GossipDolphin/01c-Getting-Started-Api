        public MainWindow()
        {
            InitializeComponent();
        }

        private void btnLoad_Click(object sender, RoutedEventArgs e)
        {
            OpenFileDialog op = new OpenFileDialog();
            op.Title = "Select a picture";
            op.Filter = "All supported graphics|*.jpg;*.jpeg;*.png|" +
              "JPEG (*.jpg;*.jpeg)|*.jpg;*.jpeg|" +
              "Portable Network Graphic (*.png)|*.png";
            if (op.ShowDialog() == true)
            {
                Actor actor = Actor.GetAllActorsFromAPI()[0];
                Debug.WriteLine(actor.ToString());
                //imgPhoto.Source = new BitmapImage(new Uri(op.FileName));
                actor.ProfilePicture = new Entertainment.Model.Image();
                actor.ProfilePicture.ImageData = getJPGFromImageControl(new BitmapImage(new Uri(op.FileName)));
                actor.ProfilePicture.ImageTitle = "snowboarder.jpg";
                Actor.AddActorToDbViaApi(actor);
            }
        }
        public byte[] getJPGFromImageControl(BitmapImage imageC)
        {
            MemoryStream memStream = new MemoryStream();
            JpegBitmapEncoder encoder = new JpegBitmapEncoder();
            encoder.Frames.Add(BitmapFrame.Create(imageC));
            encoder.Save(memStream);
            return memStream.ToArray();
        }
    }
